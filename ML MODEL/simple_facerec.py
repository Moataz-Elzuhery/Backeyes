import face_recognition
import cv2
import os
import glob
import numpy as np

class SimpleFacerec:
    def __init__(self):
        self.known_face_encodings = []
        self.known_face_names = []

        # Resize frame for faster speed
        self.frame_resizing = 0.25

    def detect_known_faces(self, frame):
        small_frame = cv2.resize(frame, (0, 0), fx=self.frame_resizing, fy=self.frame_resizing)
        rgb_small_frame = cv2.cvtColor(small_frame, cv2.COLOR_BGR2RGB)
        face_locations = face_recognition.face_locations(rgb_small_frame)
        face_encodings = face_recognition.face_encodings(rgb_small_frame, face_locations)

        face_names = []
        for face_encoding in face_encodings:
            matches = face_recognition.compare_faces(self.known_face_encodings, face_encoding)
            name = "Unknown"

            face_distances = face_recognition.face_distance(self.known_face_encodings, face_encoding)
            best_match_index = np.argmin(face_distances)
            if matches[best_match_index]:
                name = self.known_face_names[best_match_index]
            face_names.append(name)

        face_locations = np.array(face_locations)
        face_locations = face_locations / self.frame_resizing
        return face_locations.astype(int), face_names

        
    def add_known_face(self, image, name, gamma=1.0):
        resized_image = cv2.resize(image, (0, 0), fx=self.frame_resizing, fy=self.frame_resizing)
        rgb_resized_image = cv2.cvtColor(resized_image, cv2.COLOR_BGR2RGB)

    
        # Apply gamma correction
        gamma_corrected_image = adjust_gamma(rgb_resized_image, gamma)
    
        encodings = face_recognition.face_encodings(gamma_corrected_image)
    
        for encoding in encodings:
            self.known_face_encodings.append(encoding)
            self.known_face_names.append(name)


def adjust_gamma(image, gamma):
        inv_gamma = 1.0 / gamma
        table = np.array([((i / 255.0) ** inv_gamma) * 255 for i in np.arange(0, 256)]).astype(np.uint8)
        return cv2.LUT(image, table)    


